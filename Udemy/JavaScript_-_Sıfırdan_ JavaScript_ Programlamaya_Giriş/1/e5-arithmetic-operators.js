let x = 5;
let y = 2;

// SUM

let sum = x + y;
console.log("x:", x, "y:", y, "sum:", sum);
//////////////////////////////////

// SUBTRACTION

let subtract = x - y;
console.log("x:", x, "y:", y, "subtract:", subtract);
//////////////////////////////////

// MULTIPLY

let multiply = x * y;
console.log("x:", x, "y:", y, "multiply:", multiply);
//////////////////////////////////

// DIVISION

let division = x / y;
console.log("x:", x, "y:", y, "division:", division);
/////////////////////////////////

// MODULUS

let remainder = x % y;
console.log("x:", x, "y:", y, "remainder:", remainder);
/////////////////////////////////

// INCREMENT

x++;
console.log("x:", x);
////////////////////////////////

// DECREMENT

y--;
console.log("y:", y);
////////////////////////////////
